def feedback(steps: int, points: int, streak_len: int, bonus: int, days_to_next: int) -> str:
    lines = [f"Засчитано: *{steps}* шагов = *{points}* балл(а)."]
    if streak_len > 0:
        lines.append(f"Твоя серия: *{streak_len}* дн. подряд по 10 000+.")
        if bonus:
            lines.append(f"🎉 Серия 7 дней — бонус *+{bonus}* баллов!")
        elif days_to_next:
            lines.append(f"Ещё {days_to_next} дн. по 10 000+ — и бонус +4!")
    else:
        lines.append("Серия обнулилась — завтра 10 000+ и начнём заново 💪")
    return "\n".join(lines)
