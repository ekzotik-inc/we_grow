def feedback(steps: int, points: int, upd) -> str:
    """Мгновенная спортивная обратная связь после зачёта шагов."""
    fire = "🔥" if steps >= 10_000 else "👟"
    lines = [f"{fire} Засчитано: *{steps}* шагов = *{points}* балл(а)!"]
    if upd.state.current_len > 0:
        lines.append(f"🏃‍♂️ Серия: *{upd.state.current_len}* дн. подряд по 10 000+!")
        if upd.milestone == 5:
            lines.append(f"Ещё {upd.days_to_milestone} дн. — и бонус *+2* в воскресенье 💪")
        elif upd.milestone == 7:
            lines.append(f"Ещё {upd.days_to_milestone} дн. — и максимальный бонус *+4* 🥇")
        else:
            lines.append("Полная серия недели — бонус *+4* твой! 🏆")
    else:
        lines.append("Серия обнулилась — завтра 10 000+ и разгоняемся заново 🚀")
    return "\n".join(lines)


