def _keycap(digit: str) -> str:
    """Один премиум-кейкап цифры (или обычный, если премиум выкл/нет id)."""
    ch = f"{digit}⃣"
    cid = NUM_IDS.get(int(digit))
    if premium_emoji.ENABLED and cid:
        return f'<tg-emoji emoji-id="{cid}">{ch}</tg-emoji>'
    return ch


def num(n: int) -> str:
    """Номер премиум-кейкапами: двузначные склеиваются (11 → 1⃣1⃣)."""
    return "".join(_keycap(d) for d in str(n))


