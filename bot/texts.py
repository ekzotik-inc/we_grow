def feedback(steps: int, points: int, upd) -> str:
    """Мгновенная спортивная обратная связь после зачёта шагов."""
    fire = pe("🔥") if steps >= 10_000 else pe("👟")
    lines = [f"{fire} Засчитано: <b>{steps}</b> шагов = <b>{points}</b> балл(а)!"]
    if upd.state.current_len > 0:
        lines.append(f"{pe('🏃')} Серия: <b>{upd.state.current_len}</b> дн. подряд по 10 000+!")
        if upd.milestone == 5:
            lines.append(f"Ещё {upd.days_to_milestone} дн. — и бонус <b>+2</b> в воскресенье {pe('💪')}")
        elif upd.milestone == 7:
            lines.append(f"Ещё {upd.days_to_milestone} дн. — и максимальный бонус <b>+4</b> {pe('🥇')}")
        else:
            lines.append(f"Полная серия недели — бонус <b>+4</b> твой! {pe('🏆')}")
    else:
        lines.append(f"Серия обнулилась — завтра 10 000+ и разгоняемся заново {pe('🚀')}")
    return "\n".join(lines)


